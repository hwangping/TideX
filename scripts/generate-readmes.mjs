function readLocale(code) {
  return JSON.parse(fs.readFileSync(path.join(localesDir, `${code}.json`), "utf8"));
}

function wrapLanguageLinks() {
  const links = localeOrder.map((code) => {
    const locale = readLocale(code);
    return `[${locale.label}](README.${code}.md)`;
  });
  const lines = [];
  for (let index = 0; index < links.length; index += 6) {
    lines.push(links.slice(index, index + 6).join(" | "));
  }
  return lines.join("\n\n");
}

function buildOrderedList(items) {
  return items.map((item) => `1. ${item}`).join("\n");
}

function buildReadme(code) {
  const locale = readLocale(code);
  const t = translations[code];
  if (!t) {
    throw new Error(`Missing README translation for ${code}`);
  }

  return `# TideX\n\n${locale.messages.subtitle}\n\n${t.liveLabel}: [https://tide-x.vercel.app](https://tide-x.vercel.app)\n\n## ${t.overviewTitle}\n\n${t.overviewBody}\n\n## ${t.highlightsTitle}\n\n- ${t.highlights.join("\n- ")}\n\n## ${t.dataTitle}\n\n- ${t.data.join("\n- ")}\n\n## ${t.forecastTitle}\n\n${buildOrderedList(t.forecast)}\n\n## ${t.installTitle}\n\n${buildOrderedList(t.install)}\n\n## ${t.i18nTitle}\n\n- ${t.i18n.join("\n- ")}\n\n## ${t.structureTitle}\n\n\`\`\`text\n${projectTree}\n\`\`\`\n\n## ${t.developmentTitle}\n\n${t.developmentIntro}\n\n\`\`\`bash\ncd TideX\npython3 -m http.server 5173\n\`\`\`\n\n${t.developmentOpen}\n\n## ${t.deploymentTitle}\n\n- ${t.deployment.join("\n- ")}\n\n## ${t.disclaimerTitle}\n\n- ${t.disclaimer.join("\n- ")}\n`;
}

for (const code of localeOrder) {
  const content = buildReadme(code);
  fs.writeFileSync(path.join(projectRoot, `README.${code}.md`), content);
}

const rootReadme = buildReadme("en-US");
fs.writeFileSync(path.join(projectRoot, "README.md"), rootReadme);

const legacyEnglish = path.join(projectRoot, "README.en.md");
if (fs.existsSync(legacyEnglish)) {
  fs.unlinkSync(legacyEnglish);
}

